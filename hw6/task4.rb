#1 Страна с самым коротким названием
def shortest_country_name arr
  countries = []

  arr.each do |c|
     countries.push(c['loc_country'])
  end

   countries = countries.sort_by! {|i| i.length}
   return countries[0]
end

#2 Количество упоминаний страны
def countries arr
  mentioned = {}
  arr.each do |c|
     country = c['loc_country']
     mentioned[country] = mentioned.has_key?(country) ? mentioned[country] + 1 : 1
  end
  return mentioned
end

#3 Cамая близкая к Антарктиде широта
def lowest_lat arr
   latitudes = []
   arr.each do |l|
       latitudes.push(l['loc_lat'].to_f)
   end
   return latitudes.sort![0]
end

#4 Самая маленькая долгота
def lowest_lon arr
  longitudes = []
  arr.each do |l|
    longitudes.push(l['loc_lon'].to_f)
  end
  return longitudes.sort![0]
end

